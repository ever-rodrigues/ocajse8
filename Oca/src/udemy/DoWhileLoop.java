package udemy;

public class DoWhileLoop {
    public static void main(String[] args) {
        int i = 0;
        int j = 0;
        do {
            System.out.println("HEre" + i);
            i++;
        } while (i < 10);

        while (j < 10) {
            System.out.println("Test" + j);
            j++;
        }
    }
}
