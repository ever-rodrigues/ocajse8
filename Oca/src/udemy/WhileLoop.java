package udemy;

public class WhileLoop {
    public static void main(String[] args) {
        int i =0;
        int j=0;
        int x=0;
        while (i<10){
            j=i++;
            x=++i;
            System.out.println(i);
            System.out.println(j);
            System.out.println(x);

        }
    }
}
