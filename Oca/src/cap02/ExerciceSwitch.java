package cap02;

public class ExerciceSwitch {

    public static void main(String[] args) {

        String valor = "ccc";

        switch (valor){
            case "ccc":
                System.out.println("ccc");
                break;
            case "bbb":
                System.out.println("bbb");
                break;
            case "ddd":
                System.out.println("ddd");
                break;
            default:
                System.out.println("fora");
                break;
        }
    }
}
